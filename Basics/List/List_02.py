## Accessing List
thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(thislist[2:5])

list1 = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(list1[:4])

list1 = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(list1[4:])


